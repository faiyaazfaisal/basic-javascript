var price1 = 31;
var price2 = 10;

// add some value to the existing value
price1 = price1 + 10;
console.log(price1);

// addition to the same variable shorthand
// += means adding some value to the existing value
price1 +=10;

// biyoger khetre
price2 = price2 - 5;
// shorthand -=
// -= means deducting some values from the current value of the variable
price2 -= 5;
console.log(price2);

// gun  *=
price1 *= 5;
console.log(price1)

// vag /=
price1 /= 5;
console.log(price1)

